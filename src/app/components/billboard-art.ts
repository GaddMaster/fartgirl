// ─── Billboard pixel art frames ─────────────────────────────────────
// Each size has 20 frames of crypto-themed pixel art.
// Pixel values index into that frame-set's shared palette.
// 0 = transparent. Sizes: w×h in art-pixels.
// 2:2=32×16  3:2=48×16  4:2=64×16  5:2=80×16
// 3:4=48×32  4:4=64×32  5:4=80×32

export interface BillboardFrame {
    w: number;
    h: number;
    palette: string[];
    data: number[];
}

export type BillboardSizeKey = "2:2" | "3:2" | "4:2" | "5:2" | "3:4" | "4:4" | "5:4";

// ─── helper to build run-length-encoded art ─────────────────────────
function expand(w: number, h: number, runs: [number, number][]): number[] {
    const out: number[] = [];
    for (const [v, n] of runs) for (let i = 0; i < n; i++) out.push(v);
    while (out.length < w * h) out.push(0);
    return out.slice(0, w * h);
}

// Row helper: takes an array of pixel values for one row
function row(pixels: number[], w: number): number[] {
    const out = [...pixels];
    while (out.length < w) out.push(0);
    return out.slice(0, w);
}

function makeFrame(w: number, h: number, palette: string[], rows: number[][]): BillboardFrame {
    const data: number[] = [];
    for (const r of rows) data.push(...row(r, w));
    while (data.length < w * h) data.push(0);
    return { w, h, palette, data: data.slice(0, w * h) };
}

