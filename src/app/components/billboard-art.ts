// Generate 20 frames for a given billboard size using templates
function generateFrames(w: number, h: number): BillboardFrame[] {
    const frames: BillboardFrame[] = [];
    for (let i = 0; i < 20; i++) {
        const tmpl = TEMPLATES[i % TEMPLATES.length];
        const rng = seedRng(i * 7919 + w * 31 + h * 137);
        const data = tmpl.gen(w, h, rng);
        frames.push({ w, h, palette: tmpl.palette, data });
    }
    return frames;
}

